package com.example.domain;

public class PaidInfo {
	
	private int employeeId;
	private String paidPlanDate;
	private String paidAchiveDate;
	private String paidId;
	
	
	
	public int getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(int employeeId) {
		this.employeeId = employeeId;
	}
	
	public String getPaidPlanDate() {
		return paidPlanDate;
	}
	public void setPaidPlanDate(String paidPlanDate) {
		this.paidPlanDate = paidPlanDate;
	}
	public String getPaidAchiveDate() {
		return paidAchiveDate;
	}
	public void setPaidAchiveDate(String paidAchiveDate) {
		this.paidAchiveDate = paidAchiveDate;
	}
	public String getPaidId() {
		return paidId;
	}
	public void setPaidId(String paidId) {
		this.paidId = paidId;
	}
	
	
	
	

}
